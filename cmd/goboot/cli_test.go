func TestInit(t *testing.T) {
	dir := t.TempDir()
	code, out, errOut := runCLI("init", "-dir", dir)
	if code != 0 {
		t.Fatalf("init exit = %d, stderr=%s", code, errOut)
	}
	if !strings.Contains(out, "goboot.yaml") {
		t.Errorf("init output = %q", out)
	}
	if _, err := os.Stat(filepath.Join(dir, "goboot.yaml")); err != nil {
		t.Fatalf("goboot.yaml not created: %v", err)
	}
	// Second init without -force fails.
	if code, _, _ := runCLI("init", "-dir", dir); code == 0 {
		t.Errorf("second init without -force should fail")
	}
	// With -force it succeeds.
	if code, _, _ := runCLI("init", "-dir", dir, "-force"); code != 0 {
		t.Errorf("init -force should overwrite")
	}
}

func TestClean(t *testing.T) {
	dir := t.TempDir()
	// clean operates on the configured output directory (default
	// internal/generated); place the fixtures there.
	outDir := filepath.Join(dir, "internal", "generated")
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		t.Fatal(err)
	}
	generated := filepath.Join(outDir, generatedFileName)
	handWritten := filepath.Join(outDir, "handwritten.go")
	unrelatedPrefixed := filepath.Join(outDir, generatedFilePrefix+"other.go")

	// A generated file carries the marker.
	if err := os.WriteFile(generated, []byte("// Code generated by goboot. DO NOT EDIT.\npackage x\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	// A hand-written file must never be removed.
	if err := os.WriteFile(handWritten, []byte("package x\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	// A file with the prefix but no marker must not be removed.
	if err := os.WriteFile(unrelatedPrefixed, []byte("package x\n"), 0o644); err != nil {
		t.Fatal(err)
	}

	code, _, errOut := runCLI("clean", "-dir", dir)
	if code != 0 {
		t.Fatalf("clean exit = %d, stderr=%s", code, errOut)
	}
	if _, err := os.Stat(generated); !os.IsNotExist(err) {
		t.Errorf("generated file should have been removed")
	}
	if _, err := os.Stat(handWritten); err != nil {
		t.Errorf("hand-written file must not be removed")
	}
	if _, err := os.Stat(unrelatedPrefixed); err != nil {
		t.Errorf("prefixed file without the marker must not be removed")
	}
}

