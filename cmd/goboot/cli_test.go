// TestGenerateEndToEnd is the acceptance scenario of §59: run `goboot generate`
// against annotated packages and confirm the generated code compiles.
func TestGenerateEndToEnd(t *testing.T) {
	root, err := filepath.Abs(filepath.Join("..", ".."))
	if err != nil {
		t.Fatal(err)
	}
	tmp, err := os.MkdirTemp(root, "gencli")
	if err != nil {
		t.Fatal(err)
	}
	defer os.RemoveAll(tmp)

	outRel, err := filepath.Rel(root, tmp)
	if err != nil {
		t.Fatal(err)
	}

	code, out, errOut := runCLI("generate",
		"-dir", root,
		"-output", outRel,
		"-package", "wiring",
		"-verbose",
		"./compiler/testdata/diapp/...")
	if code != 0 {
		t.Fatalf("generate exit = %d\nstdout=%s\nstderr=%s", code, out, errOut)
	}

	target := filepath.Join(tmp, generatedFileName)
	if _, err := os.Stat(target); err != nil {
		t.Fatalf("generated file not written: %v", err)
	}

	// The generated package must compile (§59 step 5).
	cmd := exec.Command("go", "build", ".")
	cmd.Dir = tmp
	if buildOut, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("generated code does not compile: %v\n%s", err, buildOut)
	}
}

func TestGenerateCleanFlag(t *testing.T) {
	root, _ := filepath.Abs(filepath.Join("..", ".."))
	tmp, err := os.MkdirTemp(root, "gencli")
	if err != nil {
		t.Fatal(err)
	}
	defer os.RemoveAll(tmp)
	outRel, _ := filepath.Rel(root, tmp)

	// A stale generated file that clean should remove before regenerating.
	stale := filepath.Join(tmp, generatedFilePrefix+"stale.gen.go")
	if err := os.WriteFile(stale, []byte("// Code generated by goboot. DO NOT EDIT.\npackage wiring\n"), 0o644); err != nil {
		t.Fatal(err)
	}

	code, _, errOut := runCLI("generate",
		"-dir", root, "-output", outRel, "-package", "wiring", "-clean",
		"./compiler/testdata/diapp/...")
	if code != 0 {
		t.Fatalf("generate -clean exit = %d, stderr=%s", code, errOut)
	}
	if _, err := os.Stat(stale); !os.IsNotExist(err) {
		t.Errorf("stale generated file should have been cleaned")
	}
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}

func TestValidateWithProfile(t *testing.T) {
	// Without the profile, profile-gated components are excluded.
	_, outNo, _ := runCLI("validate", "-dir", compilerDir, "./testdata/conditions")
	// With the profile, more components are active.
	_, outProd, _ := runCLI("validate", "-dir", compilerDir, "-profile", "production", "./testdata/conditions")
	if outNo == outProd {
		t.Errorf("profile should change the active component set:\nno=%q\nprod=%q", outNo, outProd)
	}
	if !strings.Contains(outProd, "5 component") {
		t.Errorf("production profile should activate 5 components, got %q", outProd)
	}
	if !strings.Contains(outNo, "3 component") {
		t.Errorf("no profile should leave 3 components, got %q", outNo)
	}
}

